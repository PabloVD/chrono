faces = zeros(n_faces,3);


%We order the vertices starting at the bottom-left corner, row after row.
vi = 1;
for y = 1:nv_y
    verts(vi:(vi+nv_x-1),:) = [profilex,profile_width(y)*ones(nv_x,1),profile1D];
    vi = vi + nv_x;
end

% Specify triangular faces (two at a time).
% Specify the face vertices counter-clockwise.
% Set the normal indices same as the vertex indices.
it = 1;
for (iy = (nv_y - 2):-1:0) 
    for (ix = 0:(nv_x - 2))
        v0 = ix + nv_x * iy+1;
        faces(it,:) = [v0, v0 + nv_x + 1, v0 + nv_x];
        it = it + 1;
        faces(it,:) = [v0, v0 + 1, v0 + nv_x + 1];
        it = it +1;
    end
end

% Calculate normals and then average the normals from all adjacent faces.
for (it = 1:n_faces) 
    % Calculate the triangle normal as a normalized cross product.
    nrm = cross(verts(faces(it,2),:)-verts(faces(it,1),:),verts(faces(it,3),:)-verts(faces(it,1),:));
    nrm = nrm/norm(nrm);
    
    % Increment the normals of all incident vertices by the face normal
    verts_normals(faces(it,1),:) = verts_normals(faces(it,1),:) + nrm;
    verts_normals(faces(it,2),:) = verts_normals(faces(it,2),:) + nrm;
    verts_normals(faces(it,3),:) = verts_normals(faces(it,3),:) + nrm;
end

% Set the normals to the average values.

    for (ix = 0:(nv_x - 2))
        v0 = ix + nv_x * iy+1;
        faces(it,:) = [v0, v0 + nv_x + 1, v0 + nv_x];
        it = it + 1;
        faces(it,:) = [v0, v0 + 1, v0 + nv_x + 1];
